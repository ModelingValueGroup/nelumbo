package org.modelingvalue.nelumbo.lsp.intellij.lang;

import com.intellij.extapi.psi.PsiFileBase;
import com.intellij.openapi.fileTypes.FileType;
import com.intellij.psi.FileViewProvider;
import org.jetbrains.annotations.NotNull;
import org.modelingvalue.nelumbo.lsp.intellij.Constants;
import org.modelingvalue.nelumbo.lsp.intellij.NelumboFileType;

public class NelumboFile extends PsiFileBase {
    public NelumboFile(@NotNull FileViewProvider viewProvider) {
        super(viewProvider, Constants.NELUMBO);
    }

    @Override
    public @NotNull FileType getFileType() {
        return NelumboFileType.INSTANCE;
    }
}
